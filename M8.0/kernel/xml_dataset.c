		g_keyword = xmlNodeGetContent(g_xpath_obj->nodesetval->nodeTab[0]->xmlChildrenNode);
		if(xmlStrcmp(g_keyword, (xmlChar*)"999")  == 0) /* Emergency links*/
			g_data_plans[data_plan_num].ltype[l] = -4;
