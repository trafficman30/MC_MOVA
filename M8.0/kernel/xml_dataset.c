			if (XML_DataSet_WriteModifiedDataElementFromDataAreaToDoc(index, (xmlChar *)"Lane", (xmlChar *)"Oversaturation/CriticalVehicleTime", j, data_elemet_value_str) == MV_FAILURE)
				return MV_FAILURE;

			/*------ Lane/CombinationDetectors/AutoSet = False -------*/
