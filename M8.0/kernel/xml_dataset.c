static void XML_DataSet_SetDataElementValue_int(int * data_element, enum DATA_PROC  data_proc)
{

	if (g_xpath_obj->nodesetval->nodeTab == NULL)
	{
		TRACE_WRITE0_IF(LEVEL_ERROR, g_MODULE_NAME, "Unable to find data element in the XML file.");
		*data_element = 0;
		return;
	}

	g_keyword = xmlNodeGetContent(g_xpath_obj->nodesetval->nodeTab[0]->xmlChildrenNode);

	if(g_keyword != NULL)
	{
		if(data_proc == AS_IT_IS)
		{
			*data_element = atoi((char*)g_keyword);
		}
		else if(data_proc == CONVERT_TO_TENTHS)
		{
			/*MT already does the rounding, so there is no need to round to the nearest 0.1 like in MT exporting.*/
			*data_element = (int)(10*atof((char*)g_keyword));
		}
		else if(data_proc == CONVERT_TO_TENTHS_MINUS_HALF_SEC)
		{
			/*MT already does the rounding, so there is no need to round to the nearest 0.1 like in MT exporting.*/
			*data_element = (int)(10 * (atof((char*)g_keyword) -0.5));
		}
		else if(data_proc == CONVERT_TO_TENTHS_MINUS_ONE)
		{
			/*MT already does the rounding, so there is no need to round to the nearest 0.1 like in MT exporting.*/
			*data_element = (int)(10 * (atof((char*)g_keyword) - 1));
		}
		else if(data_proc == CONVERT_TO_HALF_SEC)
		{
			float real_val;
			float rounded_val;
		
			real_val = (float)atof((char*)g_keyword);

			rounded_val = (float)(0.5 * MATH_ROUND_TO_INT(real_val*2));

			*data_element = (int)(rounded_val*2);			
		}
		else if(data_proc == CONVERT_TO_OPEN_CLOSED_CONTACT)
		{
			if(strcmp((char*)g_keyword, "Open") == 0)
			{
				*data_element = 0;
			}
			else if(strcmp((char*)g_keyword, "Closed") == 0)
			{
				*data_element = 1;
			}		
			else
			{
				TRACE_WRITE0_IF( LEVEL_ERROR, g_MODULE_NAME, "Invalid data element value (should be Open or Closed).");
			}
		}
		else if (data_proc == CONVERT_TO_PERCENTAGE)
		{
			*data_element = atoi((char*)g_keyword)/100;
		}
	}
	else
	{
		TRACE_WRITE0_IF( LEVEL_ERROR, g_MODULE_NAME, "Unable to find data element in the XML file.");
	}
}

