void XML_DataSet_Initialise( void )
{
    TRACE_ASSERT( !g_isInitialised, "Dataset handler already initialised." );

    TRACE_WRITE0_IF( LEVEL_INFO, g_MODULE_NAME, 
        "Initialising dataset handler..." );   

    g_isDefaultDatasetReal = MV_FALSE;

    g_isInitialised = MV_TRUE;

    TRACE_WRITE0_IF( LEVEL_INFO, g_MODULE_NAME, 
        "Dataset handler initialised." );

} /* XML_DataSet_Initialise */


/*
    Name:           PCDataSet_DeInitialise
    Author (Date):  iabdelhalim, 02/10/2015
    Purpose:        Deinitialises the PCMOVA XML dataset module.
    Inputs:         
    Returns:        
    Remarks:
*/
void XML_DataSet_DeInitialise( void )
{
    if ( g_isInitialised )
    {
        TRACE_WRITE0_IF( LEVEL_INFO, g_MODULE_NAME, 
            "Deinitialising dataset handler..." );   

        g_isInitialised = MV_FALSE;

		if(g_doc != 0x00)
			xmlFreeDoc(g_doc);

		if(g_xpath_ctx != 0x00)
			xmlXPathFreeContext(g_xpath_ctx);

        TRACE_WRITE0_IF( LEVEL_INFO, g_MODULE_NAME, 
            "Dataset handler deinitialised." );

    }
    
} /* XML_DataSet_DeInitialise */

