		XML_DataSet_SetDataElementValue_int(&g_data_plans[data_plan_num].lmin[l], CONVERT_TO_TENTHS_MINUS_HALF_SEC);

		/*LMIN*/
		EVAL_XPATH_EXPRESSION_1a("//MOVANetwork/ControllerStream[ID='%s']/Links/Link[ID='%d']/Modelling/LinkMaximumGreen", l + 1);
		XML_DataSet_SetDataElementValue_int(&g_data_plans[data_plan_num].lnkmax[l], CONVERT_TO_TENTHS);

