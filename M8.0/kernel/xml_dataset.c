		EVAL_XPATH_EXPRESSION_1a("//MOVANetwork/ControllerStream[ID='%s']/StageLibrary/LibraryStage[ID='%d']/EmergencyAndPriority/EmergencyMaxGreenExtension", s+1);
		XML_DataSet_SetDataElementValue_int(&g_data_plans[data_plan_num].emxmax[s], CONVERT_TO_TENTHS);
		
		/*PRXMAX*/
		EVAL_XPATH_EXPRESSION_1a("//MOVANetwork/ControllerStream[ID='%s']/StageLibrary/LibraryStage[ID='%d']/EmergencyAndPriority/PriorityMaxGreenExtension", s+1);
		XML_DataSet_SetDataElementValue_int(&g_data_plans[data_plan_num].prxmax[s], CONVERT_TO_TENTHS);
		
