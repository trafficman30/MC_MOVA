static void XML_DataSet_GetDatasetFileNameFromDoc(char * dataset_filename)
{
	char * ptr;
	MVInt8 c = 0;

	/*points on the last char of the file path*/
	ptr = (char *)(g_doc->URL) + strlen((char *)(g_doc->URL)) -1;
	
	while (ptr)
	{
		if (*ptr != '/')
		{
			ptr--;
			c++;
		}
		else
			break;

		if (ptr == (char *)(g_doc->URL))
			break;
	}

	if (ptr[0] == '/')
		ptr++;
	else
		c++;
		
	strncpy(dataset_filename, ptr, c);
}

static MVInt32 XML_DataSet_GetFileSize(char * file_path_and_name)
{
	MVInt32 file_size = 0;

	FILE *fp = fopen(file_path_and_name, "r");
	fseek(fp, 0, SEEK_END);
	file_size = ftell(fp);
	fclose(fp);

	return file_size;

}

